def model_file_kind(path: Path) -> str:
    text = path.as_posix()

    if path.name == "latest_summary.json":
        return "latest_summary"

    if path.name.endswith(".metadata.json"):
        return "metadata"

    if "/checkpoints/" in text:
        return "checkpoint"

    if "/snapshots/" in text:
        return "snapshot"

    if "/saved/" in text:
        return "saved_model"

    if "/exports/" in text:
        return "export"

    return "json"


