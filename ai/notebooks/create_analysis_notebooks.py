def parse_debug_file(path: Path | None) -> dict[str, Any]:
    text = read_text(path)
    result: dict[str, Any] = {}

    for line in text.splitlines():
        if "=" not in line:
            continue

        key, value = line.split("=", 1)
        key = key.strip()
        value = value.strip()

        numeric = number_or_none(value)
        result[key] = numeric if numeric is not None else value

    result["file"] = str(path.relative_to(PROJECT_ROOT)) if path else None
    return result


def parse_sumo_tls(net_path: Path | None) -> list[dict[str, Any]]:
    if net_path is None or not net_path.exists():
        return []

    try:
        root = ET.parse(net_path).getroot()
    except ET.ParseError:
        return []

    rows: list[dict[str, Any]] = []

    for junction in root.findall("junction"):
        if junction.get("type") != "traffic_light":
            continue

        junction_id = junction.get("id", "")
        incoming_lanes = [
            lane
            for lane in junction.get("incLanes", "").split()
            if lane and not lane.startswith(":")
        ]
        internal_lanes = [
            lane
            for lane in junction.get("intLanes", "").split()
            if lane
        ]
        incoming_edges = sorted(
            {
                lane.rsplit("_", 1)[0]
                for lane in incoming_lanes
                if "_" in lane
            }
        )

        try:
            x = float(junction.get("x", "0"))
            y = float(junction.get("y", "0"))
        except ValueError:
            x = 0.0
            y = 0.0

        rows.append(
            {
                "tls_id": junction_id,
                "x": x,
                "y": y,
                "incoming_lane_count": len(incoming_lanes),
                "incoming_edge_count": len(incoming_edges),
                "internal_lane_count": len(internal_lanes),
                "incoming_edges": ",".join(incoming_edges[:12]),
            }
        )

    return rows


def count_xml_tags(path: Path | None, tag: str) -> int:
    if path is None or not path.exists():
        return 0

    try:
        root = ET.parse(path).getroot()
    except ET.ParseError:
        return 0

    return sum(1 for _ in root.iter(tag))


