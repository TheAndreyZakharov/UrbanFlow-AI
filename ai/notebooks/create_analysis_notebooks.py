def html_output(html: str) -> dict[str, Any]:
    return {
        "output_type": "display_data",
        "metadata": {},
        "data": {
            "text/html": html,
            "text/plain": strip_html(html),
        },
    }


def image_output_from_current_figure() -> dict[str, Any]:
    if plt is None:
        return text_output("matplotlib is not installed; chart image was not generated.\n")

    buffer = io.BytesIO()
    plt.tight_layout()
    plt.savefig(buffer, format="png", dpi=150, bbox_inches="tight")
    plt.close()
    encoded = base64.b64encode(buffer.getvalue()).decode("ascii")

    return {
        "output_type": "display_data",
        "metadata": {},
        "data": {
            "image/png": encoded,
            "text/plain": "<matplotlib figure>",
        },
    }


def strip_html(value: str) -> str:
    result = value.replace("<br>", "\n").replace("<br/>", "\n").replace("<br />", "\n")

    for token in [
        "<table",
        "</table>",
        "<thead>",
        "</thead>",
        "<tbody>",
        "</tbody>",
        "<tr>",
        "</tr>",
        "<th>",
        "</th>",
        "<td>",
        "</td>",
        "<h2>",
        "</h2>",
        "<h3>",
        "</h3>",
        "<p>",
        "</p>",
        "<strong>",
        "</strong>",
        "<code>",
        "</code>",
    ]:
        result = result.replace(token, " ")

    while "  " in result:
        result = result.replace("  ", " ")

    return result.strip()


def dataframe_html(rows: list[dict[str, Any]], title: str, limit: int = 80) -> dict[str, Any]:
    rows = rows[-limit:]

    if pd is not None:
        frame = pd.DataFrame(rows)
        html = f"<h2>{escape_html(title)}</h2>" + frame.to_html(index=False, escape=False)
        return html_output(html)

    html = f"<h2>{escape_html(title)}</h2>"
    html += dicts_to_html_table(rows)
    return html_output(html)


def dicts_to_html_table(rows: list[dict[str, Any]]) -> str:
    if not rows:
        return "<p>No rows.</p>"

    columns = list(rows[0].keys())

    html = "<table><thead><tr>"
    html += "".join(f"<th>{escape_html(str(column))}</th>" for column in columns)
    html += "</tr></thead><tbody>"

    for row in rows:
        html += "<tr>"
        for column in columns:
            html += f"<td>{escape_html(format_value(row.get(column)))}</td>"
        html += "</tr>"

    html += "</tbody></table>"
    return html


def escape_html(value: str) -> str:
    return (
        value.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def format_value(value: Any) -> str:
    if value is None:
        return "—"

    if isinstance(value, float):
        return f"{value:.4f}"

    return str(value)


def latest_file(pattern: str) -> Path | None:
    files = sorted(
        PROJECT_ROOT.glob(pattern),
        key=lambda path: path.stat().st_mtime if path.exists() else 0,
    )
    return files[-1] if files else None


def all_files(pattern: str) -> list[Path]:
    return sorted(
        PROJECT_ROOT.glob(pattern),
        key=lambda path: path.stat().st_mtime if path.exists() else 0,
    )


def latest_existing(paths: list[Path]) -> Path | None:
    candidates = [path for path in paths if path.exists()]

    if not candidates:
        return None

    return max(candidates, key=lambda path: path.stat().st_mtime)


def read_text(path: Path | None) -> str:
    if path is None or not path.exists():
        return ""

    try:
        return path.read_text(encoding="utf-8")
    except Exception as error:
        return f"Could not read {path}: {error!r}"


def read_json(path: Path | None) -> dict[str, Any]:
    if path is None or not path.exists():
        return {}

    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception as error:
        return {"error": repr(error), "path": str(path)}


def read_csv_rows(path: Path | None) -> list[dict[str, Any]]:
    if path is None or not path.exists():
        return []

    try:
        with path.open("r", newline="", encoding="utf-8") as file:
            return list(csv.DictReader(file))
    except Exception:
        return []


def read_jsonl_rows(path: Path | None) -> list[dict[str, Any]]:
    if path is None or not path.exists():
        return []

    result: list[dict[str, Any]] = []

    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.strip()

        if not line:
            continue

        try:
            result.append(json.loads(line))
        except json.JSONDecodeError:
            continue

    return result


def number_or_none(value: Any) -> float | None:
    if value is None or value == "":
        return None

    try:
        return float(value)
    except (TypeError, ValueError):
        return None


def int_or_none(value: Any) -> int | None:
    number = number_or_none(value)

    if number is None:
        return None

    return int(number)


def normalize_numeric_rows(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    numeric_keys = {
        "current_step",
        "current_episode",
        "current_vehicles",
        "best_reward",
        "latest_reward",
        "average_wait_time",
        "average_vehicle_wait_time",
        "congestion_score",
        "stopped_vehicles",
        "active_vehicles",
        "active_pedestrians",
        "average_speed_mps",
        "throughput",
        "tls_count",
        "route_vehicle_count",
        "route_flow_count",
        "route_person_count",
        "pt_stop_count",
        "pt_vehicle_count",
        "pt_flow_count",
        "sumo_net_tls_count",
        "tls_junction_count",
    }

    normalized: list[dict[str, Any]] = []

    for row in rows:
        item = dict(row)

        for key in numeric_keys:
            if key in item:
                item[key] = number_or_none(item.get(key))

        normalized.append(item)

    return normalized


