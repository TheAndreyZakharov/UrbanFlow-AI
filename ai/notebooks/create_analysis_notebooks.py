def build_04_evaluate_tls_agent() -> list[dict[str, Any]]:
    artifacts = discover_artifacts()
    model_rows = load_model_registry_rows(artifacts)
    all_history = load_all_training_history(artifacts)
    saved_model_payloads = []

    for path in artifacts["saved_model_files"]:
        payload = read_json(path)
        saved_model_payloads.append(
            {
                "file": str(path.relative_to(PROJECT_ROOT)),
                "model_type": payload.get("model_type"),
                "model_version": payload.get("model_version"),
                "training_steps": payload.get("training_steps"),
                "best_reward": payload.get("best_reward"),
                "loaded_model_path": payload.get("loaded_model_path"),
                "signal_scope": payload.get("signal_scope") or payload.get("metadata", {}).get("signal_scope"),
            }
        )

    saved_model_payloads = normalize_numeric_rows(saved_model_payloads)

    outputs: list[dict[str, Any]] = [
        dataframe_html(model_rows, "All model registry files", limit=160),
        dataframe_html(saved_model_payloads, "Saved runtime model checkpoints", limit=80),
        dataframe_html(all_history, "All training history", limit=150),
    ]

    for metric in ["best_reward", "latest_reward", "average_wait_time", "congestion_score", "stopped_vehicles", "current_vehicles"]:
        metric_rows = [row for row in model_rows if row.get(metric) is not None]

        if not metric_rows:
            continue

        labels = [
            f"{row.get('signal_scope') or 'unknown'} / {row.get('kind')}"
            for row in metric_rows[-40:]
        ]
        values = [row.get(metric) for row in metric_rows[-40:]]

        outputs.append(
            plot_bar(
                f"Model artifact comparison: {metric}",
                labels,
                values,
                ylabel=metric,
            )
        )

    if all_history:
        scopes = sorted({str(row.get("signal_scope")) for row in all_history if row.get("signal_scope") is not None})

        for metric in ["latest_reward", "best_reward", "average_wait_time", "congestion_score", "stopped_vehicles"]:
            series: dict[str, list[Any]] = {}
            xs_by_scope: dict[str, list[Any]] = {}

            for scope in scopes:
                scope_rows = [
                    row
                    for row in all_history
                    if str(row.get("signal_scope")) == scope and row.get("current_step") is not None
                ]
                scope_rows.sort(key=lambda row: float(row.get("current_step") or 0))

                if not scope_rows:
                    continue

                xs_by_scope[scope] = metric_values(scope_rows, "current_step")
                series[scope] = metric_values(scope_rows, metric)

            if series:
                if len(series) == 1:
                    only_scope = next(iter(series))
                    outputs.append(
                        plot_line(
                            f"Training evaluation: {metric} ({only_scope})",
                            xs_by_scope[only_scope],
                            {only_scope: series[only_scope]},
                            xlabel="Training step",
                            ylabel=metric,
                        )
                    )
                else:
                    for scope, values in series.items():
                        outputs.append(
                            plot_line(
                                f"Training evaluation by scope: {metric} / {scope}",
                                xs_by_scope[scope],
                                {scope: values},
                                xlabel="Training step",
                                ylabel=metric,
                            )
                        )

        outputs.extend(
            [
                plot_scatter(
                    "Evaluation scatter: reward vs wait time",
                    metric_values(all_history, "average_wait_time"),
                    metric_values(all_history, "latest_reward"),
                    xlabel="Average wait time",
                    ylabel="Latest reward",
                ),
                plot_scatter(
                    "Evaluation scatter: reward vs stopped vehicles",
                    metric_values(all_history, "stopped_vehicles"),
                    metric_values(all_history, "latest_reward"),
                    xlabel="Stopped vehicles",
                    ylabel="Latest reward",
                ),
                plot_scatter(
                    "Evaluation scatter: reward vs congestion",
                    metric_values(all_history, "congestion_score"),
                    metric_values(all_history, "latest_reward"),
                    xlabel="Congestion score",
                    ylabel="Latest reward",
                ),
            ]
        )

    latest_summary = read_json(artifacts["latest_summary"])
    latest_checkpoint = read_json(artifacts["latest_checkpoint"])

    outputs.extend(
        [
            html_output("<h2>Latest summary JSON</h2><pre>" + escape_html(json.dumps(latest_summary, indent=2, ensure_ascii=False)[:12000]) + "</pre>"),
            html_output("<h2>Latest checkpoint JSON</h2><pre>" + escape_html(json.dumps(latest_checkpoint, indent=2, ensure_ascii=False)[:12000]) + "</pre>"),
        ]
    )

    return [
        markdown_cell(
            """
            ## Executed evaluation dashboard

            This dashboard compares saved artifacts, checkpoints and training histories.
            """
        ),
        executed_code_cell(
            "# UrbanFlow AI generated evaluation dashboard. Outputs are embedded by create_analysis_notebooks.py.\n",
            outputs,
            execution_count=1,
        ),
    ]


