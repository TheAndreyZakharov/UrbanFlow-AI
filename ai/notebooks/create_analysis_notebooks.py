def stats_rows(rows: list[dict[str, Any]], columns: list[str]) -> list[dict[str, Any]]:
    result: list[dict[str, Any]] = []

    for column in columns:
        values = [
            float(row[column])
            for row in rows
            if row.get(column) is not None
        ]

        if not values:
            continue

        result.append(
            {
                "metric": column,
                "count": len(values),
                "min": min(values),
                "mean": statistics.mean(values),
                "median": statistics.median(values),
                "max": max(values),
                "last": values[-1],
            }
        )

    return result


