def load_all_training_history(artifacts: dict[str, Any]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []

    for path in artifacts["history_csv_files"]:
        for row in read_csv_rows(path):
            row = dict(row)
            row["history_file"] = str(path.relative_to(PROJECT_ROOT))
            rows.append(row)

    if not rows:
        for path in artifacts["history_jsonl_files"]:
            for row in read_jsonl_rows(path):
                row = dict(row)
                row["history_file"] = str(path.relative_to(PROJECT_ROOT))
                rows.append(row)

    return normalize_numeric_rows(rows)


